//assignment operator
let r=5;

//addition assignment operator
r += 1; // r=r+1
console.log(r)

//subtraction assignment operator
r-=1;//r=r-1
console.log(r)

//multiplacaton operator
r *= 1;//r=r*1
console.log(r);//5

//division operator
r/=2;//r=r/2
console.log(r)//2.5

//remainder(modulus) assignment operator
r%= 2; //r=r % 2
console.log(r)//.5

//exponential assignment operator
r**=1//r=r**1
console.log(r)//0.5